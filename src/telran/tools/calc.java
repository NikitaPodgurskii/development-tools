package telran.tools;

public class calc {
	public static double sum(double op1, double op2) 
	{
		return op1 + op2;
	}
	public static double subctract(double op1, double op2) 
	{
		return op1 - op2;
	}
	public static double multipied(double op1, double op2) 
	{
		return op1 * op2;
	}
	public static double divide(double op1, double op2) 
	{
		return op1 / op2;
	}
}

